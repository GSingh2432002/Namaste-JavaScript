`Callback Hell & Inversion of Control`
/*
Callback Hell is essentially nested callbacks stacked below one another forming a pyramid structure. Every callback depends/waits for the previous callback, thereby making a pyramid structure that affects the readablility and maintainability of the code. 

In the context of callbacks, inversion of control is the notion of having code under your control in one part of the program, then handling control over to a callback in another part of the program.
*/